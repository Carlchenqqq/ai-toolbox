export const categories: Category[] = categoryRaw.map(cat => ({
  ...cat,
  count: tools.filter(t => t.category === cat.slug).length,
}));

export function getToolById(id: string): AITool | undefined {
  return tools.find(t => t.id === id);
}

export function getToolsByCategory(slug: string): AITool[] {
  return tools.filter(t => t.category === slug);
}

export function getFeaturedTools(): AITool[] {
  return tools.filter(t => t.featured);
}

// Simple client-side search (used by HomeClient)
export function simpleSearch(query: string, toolList: AITool[]): AITool[] {
  if (!query.trim()) return toolList;
  const q = query.toLowerCase().trim();
  return toolList.filter(t =>
    t.name.toLowerCase().includes(q) ||
    t.nameCN.toLowerCase().includes(q) ||
    t.descriptionCN.toLowerCase().includes(q) ||
    t.description.toLowerCase().includes(q) ||
    t.tags.some(tag => tag.toLowerCase().includes(q)) ||
    t.category.toLowerCase().includes(q)
  );
}

